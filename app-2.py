import streamlit as st
import numpy as np
import joblib

# Load the trained model
model = joblib.load('airbnb_price_predictor.pkl')

st.title("üè† Airbnb Price Predictor")
st.write("Enter listing details to predict the nightly price.")

# Input fields
bedrooms = st.number_input("Bedrooms", min_value=0.0, value=1.0)
bathrooms = st.number_input("Bathrooms", min_value=0.0, value=1.0)
minimum_nights = st.number_input("Minimum Nights", min_value=1, value=2)
number_of_reviews = st.number_input("Number of Reviews", min_value=0, value=10)
availability_365 = st.number_input("Availability (days per year)", min_value=0, max_value=365, value=180)
review_scores_rating = st.slider("Review Score Rating", min_value=0.0, max_value=100.0, value=80.0)

# Prediction
if st.button("Predict Price"):
    input_data = np.array([[bedrooms, bathrooms, minimum_nights,
                            number_of_reviews, availability_365, review_scores_rating]])
    prediction = model.predict(input_data)
    st.success(f"üí∞ Estimated Nightly Price: ${prediction[0]:.2f}")